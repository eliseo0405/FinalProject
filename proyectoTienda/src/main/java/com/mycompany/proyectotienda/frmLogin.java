    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        // TODO add your handling code here:
        String[] Usuarios = {"Hamlet", "Eliseo", "Melvin", "Hector"};
        String[] Claves = new String[4];
        Claves[0] = "12345"; //CLAVE HAMLET
        Claves[1] = "8179"; //CLAVE ELISEO
        Claves[2] = "d93bf2ad8d#"; //CLAVE MELVIN
        Claves[3] = "211713"; //CLAVE HECTOR
        
        String usuario = txtusuario.getText();
        String password = contraseña.getText();
        boolean mensaje=false;
        
        for(int i=0;i<Usuarios.length;i++){
            if(Usuarios[i].equals(usuario)&&Claves[i].equals(password)){
                mensaje=true;
            }
        }
        if(mensaje){
            frmSistemaTienda frm = new frmSistemaTienda();
            frm.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null,"Usuario o Contraseña Incorrecta");
            txtusuario.setText(""); 
            contraseña.setText("");
        }
                
    }//GEN-LAST:event_btningresarActionPerformed

